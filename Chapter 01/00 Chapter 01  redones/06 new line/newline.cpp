/*
   This program demonstrates the \n code, which 
   generates a new line. 
*/ 
#include <iostream> 
using namespace std; 
 
int main() 
{ 
  cout << "\n on\n e\n"; 
  cout << "two\n"; 
  cout << "three"; 
  cout << "four"; 
  cout << "\n"; // print a blank line 
  system ("PAUSE");  
 
  return 0; 
}
